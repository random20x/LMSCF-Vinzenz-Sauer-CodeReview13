<?php

namespace AppBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * bigevents
 *
 * @ORM\Table(name="bigevents")
 * @ORM\Entity(repositoryClass="AppBundle\Repository\bigeventsRepository")
 */
class bigevents
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
